'''
Assignment Operator: It basically assigns the values from right to left.
a=b
a=a+b or a+=b
a=a-b or a-=b
a=a/b or a/=b
a=a*b or a*=b
a=a**b or a**=b
a=a//b or a//=b

All these will update the value of variable a
'''
