'''
Bitwise Operators:
Binary AND              a & b
Binary OR               a | b
Binary XOR              a ^ b
Binary NOT              ~a
Binary LEFT SHIFT       a << b
Binary RIGHT SHIFT      a >> b
'''
a=5
b=2
print(a & b)            #0
print(a | b)            #7
print(a ^ b)            #7
print(~a)               #-6
print(~b)               #-3
print(a << b)           #20
print(a >> b)           #1
