'''
Comparison Operator: Comparison operator will always give you binomial output.

Equal to:               ==
Not Equal to:           !=
Greater than:           >
Less than:              <
Greater than equal to:  >=
Less than equal to:     <=
'''
var_1=100
var_2=200

print(var_1==var_2)     #False
print(var_1!=var_2)     #True
print(var_1>var_2)      #False
print(var_1<var_2)      #True
print(var_1>=var_2)     #False
print(var_1<=var_2)     #True