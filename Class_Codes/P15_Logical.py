'''
Logical Operators: It is based on Logics gates operations:
and: a and b<-----Returns a if a is False, b otherwise
or: a and b<-----Returns a if a is True, b otherwise
not: not a<-----Returns True if a is True, False otherwise
'''
a=100
b=0

print(a and b)      #0
print(a or b)       #100
print(not a)        #False
print(not b)        #True